
package com.jdbc;

public class VowelConsonant {

    public static void main(String[] args) {

        char ch = 'A';  // Use single quotes for character literals

        if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u'
            || ch == 'A' || ch == 'E' || ch == 'I' || ch == 'O' || ch == 'U') {

            System.out.println(ch + " is a vowel.");  // Use double quotes for string literals

        } 
        else {

            System.out.println(ch + " is a consonant.");  // Use double quotes for string literals

        }

    }
}


	
